class Solution {
  public:
    int solve(int N, vector<int> A) {
        int ans=-1;
        for(int i=N-1;i>=0;i--){
            if(A[i]>=0 && A[i]<=8){
                ans=i+1;
                break;
            }
        }
        return ans;
    }
};

