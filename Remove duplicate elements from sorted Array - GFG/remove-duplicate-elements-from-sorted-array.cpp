class Solution{
public:
    int remove_duplicate(int a[],int n){
        if(n==1) return n;
        
        int i=1,ptr=1;
        while(ptr<n){
            int num=a[i-1];
            while(ptr<n && a[ptr]==num) ptr++;
            if(ptr==n) break;
            a[i] = a[ptr];
            i++; ptr++;
        }
        
        return i;
    }
};

