class Solution
{
    public:
    void rearrangeEvenOdd(Node *head)
    {
       if(head->next==NULL) return;
       
       Node* temp1 = head;
       Node* temp2 = head->next;
       
       Node* head2 = head->next;
       
       while(temp1!=NULL && temp2!=NULL){
           temp1->next = temp2->next;
           temp1 = temp1->next;
           
           if(temp1==NULL) break;
           
           temp2->next = temp1->next;
           temp2 = temp2->next;
       }
       
       temp1 = head;
       while(temp1->next!=NULL){
           temp1 = temp1->next;
       }
       
       temp1->next = head2;
       
    }
};


